package com.poly_store_online.model;

public class GioHang {
    int maspGH;
    String tenspGH;
    long giaspGH;
    String hinhspGH;
    int soluongGH;

    public GioHang() {
    }

    public int getMaspGH() {
        return maspGH;
    }

    public void setMaspGH(int maspGH) {
        this.maspGH = maspGH;
    }

    public String getTenspGH() {
        return tenspGH;
    }

    public void setTenspGH(String tenspGH) {
        this.tenspGH = tenspGH;
    }

    public long getGiaspGH() {
        return giaspGH;
    }

    public void setGiaspGH(long giaspGH) {
        this.giaspGH = giaspGH;
    }

    public String getHinhspGH() {
        return hinhspGH;
    }

    public void setHinhspGH(String hinhspGH) {
        this.hinhspGH = hinhspGH;
    }

    public int getSoluongGH() {
        return soluongGH;
    }

    public void setSoluongGH(int soluongGH) {
        this.soluongGH = soluongGH;
    }

}
